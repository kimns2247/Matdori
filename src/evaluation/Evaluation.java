package evaluation;

public class Evaluation {
	private int bbsID;
	private String ID;
	private int likeEat;
	private int sosoEat;
	private int badEat;
	public int getBbsID() {
		return bbsID;
	}
	public void setBbsID(int bbsID) {
		this.bbsID = bbsID;
	}
	public String getID() {
		return ID;
	}
	public void setID(String ID) {
		this.ID = ID;
	}
	public int getLikeEat() {
		return likeEat;
	}
	public void setLikeEat(int likeEat) {
		this.likeEat = likeEat;
	}
	public int getSosoEat() {
		return sosoEat;
	}
	public void setSosoEat(int sosoEat) {
		this.sosoEat = sosoEat;
	}
	public int getBadEat() {
		return badEat;
	}
	public void setBadEat(int badEat) {
		this.badEat = badEat;
	}
	
	
}
